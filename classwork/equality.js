// Difference between == and === in JS

console.log("Using == operator for 5 and '5':")
console.log(5 == '5')  
console.log("true because values are equal irrespective of type\n")

console.log("Using === operator for 5 and '5':")
console.log(5 === '5') 
console.log("false because types are different (type and value both must be same for ===)")