// Convert the following to Number, Boolean, and String

let a = "50";
let b = "hello";
let c = false;
let d = null;
let e = "100px";

console.log("Original Values:");
console.log("a:", a, "b:", b, "c:", c, "d:", d, "e:", e);

// Convert to Number
let numA = Number(a);
let numB = Number(b);
let numC = Number(c);
let numD = Number(d);
let numE = Number(e);

console.log("\nConverted to Number:");
console.log("a:", numA, "b:", numB, "c:", numC, "d:", numD, "e:", numE);

// Convert to Boolean
let boolA = Boolean(a);
let boolB = Boolean(b);
let boolC = Boolean(c);
let boolD = Boolean(d);
let boolE = Boolean(e);

console.log("\nConverted to Boolean:");
console.log("a:", boolA, "b:", boolB, "c:", boolC, "d:", boolD, "e:", boolE);

// convert to String
let strA = String(a);
let strB = String(b);
let strC = String(c);
let strD = String(d);
let strE = String(e);

console.log("\nConverted to String:");
console.log("a:", strA, "b:", strB, "c:", strC, "d:", strD, "e:", strE);
